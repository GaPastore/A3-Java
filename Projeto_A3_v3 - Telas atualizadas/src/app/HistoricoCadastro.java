    private void btFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btFiltrarActionPerformed
        // TODO add your handling code here:
        String email = txtEmail.getText();
        String nome = txtNome.getText();
        String cep = txtCep.getText();
        
        Usuario usuario = new Usuario(nome, email, null, null, null, null, null, cep, null, null, null, null, null, 1);
        DAO dao = new DAO();
        
        try {
        
            if(dao.consultaUsuario(usuario)){
                tbHistorico.setValueAt(usuario.getNome(), 0, 1);
                tbHistorico.setValueAt(usuario.getCep(), 0, 4);
            }
        
        } catch (Exception e){
            
            JOptionPane.showMessageDialog(null, "Problemas t√©cnicos!");
            e.printStackTrace();
            
        }
        
    }//GEN-LAST:event_btFiltrarActionPerformed

